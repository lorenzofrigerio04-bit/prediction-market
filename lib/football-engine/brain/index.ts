/**
 * BRAIN — Layer 2 of the Football Intelligence Engine.
 *
 * Orchestrates 4 specialized agents in sequence:
 * 1. ANALYST: finds patterns and correlations from RADAR data
 * 2. CREATIVE: generates "wow" event ideas from patterns + context
 * 3. VERIFIER: validates feasibility, resolution sources, edge cases
 * 4. RESOLVER: pre-computes exact resolution strategy for each event
 *
 * Input: RadarOutput (from Layer 1)
 * Output: BrainOutput (verified events ready for FORGE to structure as markets)
 */

import type { FootballSignal, MatchContext, RadarOutput } from "../types";
import {
  runAnalyst,
  runCreative,
  runVerifier,
  runResolver,
  type AnalystInsight,
  type CreativeEvent,
  type VerifiedEvent,
  type ResolutionStrategy,
} from "./agents";

// ---------------------------------------------------------------------------
// Output types
// ---------------------------------------------------------------------------

export interface ApprovedEvent {
  creative: CreativeEvent;
  verification: VerifiedEvent;
  resolution: ResolutionStrategy | null;
  matchContext: MatchContext | null;
}

export interface BrainOutput {
  timestamp: string;
  /** Total matches analyzed */
  matchesAnalyzed: number;
  /** Insights found by analyst */
  insights: AnalystInsight[];
  /** All ideas generated by creative */
  totalIdeas: number;
  /** Events that passed verification */
  approvedEvents: ApprovedEvent[];
  /** Rejected events (for observability) */
  rejectedCount: number;
  /** Per-agent timing and model info */
  diagnostics: {
    analystInsightCount: number;
    creativeIdeaCount: number;
    verifierApprovedCount: number;
    verifierRejectedCount: number;
    resolverStrategyCount: number;
    totalDurationMs: number;
  };
}

// ---------------------------------------------------------------------------
// Orchestrator
// ---------------------------------------------------------------------------

export interface BrainOptions {
  /** Max matches to process (default: 15) */
  maxMatches?: number;
  /** Minimum interest score to process a match (default: 20) */
  minInterestScore?: number;
  /** Skip resolver for faster runs (default: false) */
  skipResolver?: boolean;
}

export async function runBrain(
  radarOutput: RadarOutput,
  options?: BrainOptions
): Promise<BrainOutput> {
  const startTime = Date.now();
  const maxMatches = options?.maxMatches ?? 15;
  const minInterest = options?.minInterestScore ?? 20;
  const skipResolver = options?.skipResolver ?? false;

  // Filter to interesting matches only
  const matches = radarOutput.matches
    .filter((m) => m.interestScore >= minInterest)
    .slice(0, maxMatches);

  console.log(
    `[BRAIN] Starting with ${matches.length} matches (filtered from ${radarOutput.matches.length}, minInterest=${minInterest})`
  );

  if (matches.length === 0) {
    return emptyOutput(startTime, 0);
  }

  // ── Step 1: ANALYST ──────────────────────────────────────────
  console.log("[BRAIN] Step 1/4: Running Analyst...");
  let insights: AnalystInsight[] = [];
  try {
    insights = await runAnalyst(matches);
  } catch (err) {
    console.error("[BRAIN] Analyst failed:", err instanceof Error ? err.message : err);
  }

  // ── Step 2: CREATIVE ─────────────────────────────────────────
  console.log("[BRAIN] Step 2/4: Running Creative...");
  let creativeEvents: CreativeEvent[] = [];
  try {
    creativeEvents = await runCreative(matches, insights, radarOutput.floatingSignals);
  } catch (err) {
    console.error("[BRAIN] Creative failed:", err instanceof Error ? err.message : err);
  }

  if (creativeEvents.length === 0) {
    console.warn("[BRAIN] Creative produced 0 events, stopping.");
    return emptyOutput(startTime, matches.length, insights);
  }

  // ── Step 3: VERIFIER ─────────────────────────────────────────
  console.log("[BRAIN] Step 3/4: Running Verifier...");
  let verifications: VerifiedEvent[] = [];
  try {
    verifications = await runVerifier(creativeEvents, matches);
  } catch (err) {
    console.error("[BRAIN] Verifier failed:", err instanceof Error ? err.message : err);
  }

  const approvedVerifications = verifications.filter((v) => v.approved);
  const rejectedCount = verifications.filter((v) => !v.approved).length;

  if (approvedVerifications.length === 0) {
    console.warn("[BRAIN] Verifier approved 0 events, stopping.");
    return {
      timestamp: new Date().toISOString(),
      matchesAnalyzed: matches.length,
      insights,
      totalIdeas: creativeEvents.length,
      approvedEvents: [],
      rejectedCount,
      diagnostics: {
        analystInsightCount: insights.length,
        creativeIdeaCount: creativeEvents.length,
        verifierApprovedCount: 0,
        verifierRejectedCount: rejectedCount,
        resolverStrategyCount: 0,
        totalDurationMs: Date.now() - startTime,
      },
    };
  }

  // ── Step 4: RESOLVER ─────────────────────────────────────────
  let resolutionStrategies: ResolutionStrategy[] = [];
  if (!skipResolver) {
    console.log("[BRAIN] Step 4/4: Running Resolver...");
    try {
      resolutionStrategies = await runResolver(creativeEvents, verifications);
    } catch (err) {
      console.error("[BRAIN] Resolver failed:", err instanceof Error ? err.message : err);
    }
  } else {
    console.log("[BRAIN] Step 4/4: Resolver skipped.");
  }

  // ── Assemble output ──────────────────────────────────────────
  const approvedEvents: ApprovedEvent[] = approvedVerifications.map((v) => {
    const creative = creativeEvents[v.eventId];
    const resolution = resolutionStrategies.find((r) => r.eventId === v.eventId) ?? null;
    const matchContext = creative?._matchId
      ? matches.find((m) => m.match.id === creative._matchId) ?? null
      : null;

    return {
      creative: creative ?? ({} as CreativeEvent),
      verification: v,
      resolution,
      matchContext,
    };
  });

  const totalDurationMs = Date.now() - startTime;
  console.log(
    `[BRAIN] Complete in ${(totalDurationMs / 1000).toFixed(1)}s — ${approvedEvents.length} events approved from ${creativeEvents.length} ideas`
  );

  return {
    timestamp: new Date().toISOString(),
    matchesAnalyzed: matches.length,
    insights,
    totalIdeas: creativeEvents.length,
    approvedEvents,
    rejectedCount,
    diagnostics: {
      analystInsightCount: insights.length,
      creativeIdeaCount: creativeEvents.length,
      verifierApprovedCount: approvedVerifications.length,
      verifierRejectedCount: rejectedCount,
      resolverStrategyCount: resolutionStrategies.length,
      totalDurationMs,
    },
  };
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function emptyOutput(
  startTime: number,
  matchesAnalyzed: number,
  insights: AnalystInsight[] = []
): BrainOutput {
  return {
    timestamp: new Date().toISOString(),
    matchesAnalyzed,
    insights,
    totalIdeas: 0,
    approvedEvents: [],
    rejectedCount: 0,
    diagnostics: {
      analystInsightCount: insights.length,
      creativeIdeaCount: 0,
      verifierApprovedCount: 0,
      verifierRejectedCount: 0,
      resolverStrategyCount: 0,
      totalDurationMs: Date.now() - startTime,
    },
  };
}
